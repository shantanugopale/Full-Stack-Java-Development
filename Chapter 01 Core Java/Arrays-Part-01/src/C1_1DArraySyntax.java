
public class C1_1DArraySyntax {

	public static void main(String[] args) {
//		Array is a Index base data Structure and 
//		we stored Huge Amount of Data in a Single Variable.
//		In Java, Array is referred as a Object.
		
//		Syntax : By using new keyword we Creates an Array.
		
		int[] arr = new int[2];
		arr[0] = 1;
		System.out.println(arr[0]);
		arr[1] = 2;
		System.out.println(arr[1]);
		
	}

}
